"""
+  - adição
-  - subtração
*  - multiplicação
** - potenciação
/  - divisão retorna valor real
// - divisão retorna valor inteiro
%  - verificador de resto
() - precedência de valores
"""
print('### INICIO DO POLIFORMISMO ###')
print('Adicao e o resultado de 20 + 20: ', 20+20)         
print('Adicao e o resultado de 20 + 20: ', '20'+'20')
print('Multiplicacao e o resultado de 2*2: ', 2*2)
print('Multiplicacao e o resultado de 2*2: ', '2'*2)
print('### FIM DO POLIFORMISTMO ###')
print('Subtracao e o resultado de 20-20: ', 20-20)
print('Divisao com valor REAL e o resultado de 20/2: ', 20/2)
print('Divisao com valor INT. e o resultado de 20//2: ', 20//2)
print('Se usa % para verificar o resto da divisao de 20%5: ', 20%5)
print('Se usa ** para executar uma potenciacao como por exemplo 10**2: ', 10**2)
print('Precedencia com parentesis na expressao 5*(2+10)-3: ', 5*(2+10)-3)
